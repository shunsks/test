uint16_t len = 0;
uint8_t crcchar = 0;
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    uint16_t i;
    
    if (htim == (&htim3))
    {
        timcnt++;
        if(timcnt>=20)
        {
            
            len = USART_RX_STA&0x3fff;
            
            for(i = 0;i<(len-2);i++)
            {
                crcchar +=  USART_RX_BUF[i];
            }
            
            timcnt = 0;
            if(crcchar == USART_RX_BUF[len-1])
            {
                USART_RX_STA |= 0x8000;
                crcchar = 0;
            }
            HAL_TIM_Base_Stop_IT(&htim3);
        }
    }
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance==USART1)//如果是串口1
	{   timcnt = 0;
		if((USART_RX_STA&0x8000)==0)//接收未完成
		{
//            HAL_UART_Transmit(&huart1,(uint8_t*)aRxBuffer[0],1,1000);
			if(USART_RX_STA&0x4000)
			{
				USART_RX_BUF[USART_RX_STA&0X3FFF]=aRxBuffer[0] ;
                USART_RX_STA++;
          	
			}
			else 
			{	
				if(aRxBuffer[0]==0x82)
                {
                    HAL_TIM_Base_Start_IT(&htim3);
                    USART_RX_STA |= 0x4000;
                    USART_RX_BUF[USART_RX_STA&0X3FFF]=aRxBuffer[0] ;
                    USART_RX_STA++;
                    
                }
				else
				{
                  USART_RX_STA=0;
				}		 
			}
		}

	}
}
 
